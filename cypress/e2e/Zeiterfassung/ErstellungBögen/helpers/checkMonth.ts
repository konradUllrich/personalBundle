export const checkMonat = (monat: ZE_T_MONAT, { year }: { year: string }) => {
  const compareMonth = monate.find((_m) => monat.L_MONAT === _m.L_MONAT);
  const checkfields: (keyof ZE_T_MONAT)[] = [
    "STR_MONAT",
    "L_MONAT",
    "REF_FREIGABESTATUS",
    "STR_ZEITIST",
    "STR_MITARBEITERIN",
    "STR_FUEHRUNGSKRAFT",
  ];
  checkfields.forEach((key) => {
    //@ts-ignore
    expect(monat[key], key).to.eq(compareMonth[key]);
  });
  expect(monat.L_JAHR, "Jahr").to.eql(parseInt(year));
  expect(monat.L_MONAT, "Monat").to.eql(
    parseInt(moment().month(monat.STR_MONAT).format("M"))
  );
};
